import { factorize } from "../Factorial/factorial.js";


/**
 * Calculate permutation
 * 
 * @param {Number} n The first number 
 * @param {Number} k The divider
 * @returns {Number} The result of permutational set.
 */
const permutate = (n, k) => {
    let m = n - k ;
    return factorize(n) / factorize(m);
}

export {permutate};

